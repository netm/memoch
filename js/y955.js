  const todayBtn = document.getElementById("todayBtn");
  const methodTitle = document.getElementById("methodTitle");
  const methodBody = document.getElementById("methodBody");
  const copyBtn = document.getElementById("copyBtn");
  const pngBtn = document.getElementById("pngBtn");
  const xBtn = document.getElementById("xBtn");
  const fbBtn = document.getElementById("fbBtn");
  const lineBtn = document.getElementById("lineBtn");
  const mailBtn = document.getElementById("mailBtn");
  const listContainer = document.getElementById("listContainer");

  let lastIndex = -1;
  function pickRandomIndex() {
    if (methods.length === 0) return -1;
    let i;
    if (methods.length === 1) return 0;
    do { i = Math.floor(Math.random() * methods.length); } while (i === lastIndex);
    lastIndex = i;
    return i;
  }

  function showMethod(i) {
    if (i < 0 || i >= methods.length) return;
    methodTitle.innerHTML = methods[i].name;
    methodBody.innerHTML = methods[i].text;
  }

  // 初期表示
  showMethod(0);

  // 今日の節約ダイエットボタン（位置をHTMLで移動したがIDは変わらないためそのまま利用）
  todayBtn.addEventListener("click", () => {
    const idx = pickRandomIndex();
    showMethod(idx);
    document.getElementById("methodWrap").scrollIntoView({ behavior: "smooth", block: "center" });
  });

  // コピー機能
  copyBtn.addEventListener("click", async () => {
    const text = methodTitle.textContent + "\n" + methodBody.textContent;
    try {
      await navigator.clipboard.writeText(text);
      flash(copyBtn, "コピーしました");
    } catch (e) {
      const ta = document.createElement("textarea");
      ta.value = text;
      document.body.appendChild(ta);
      ta.select();
      document.execCommand("copy");
      ta.remove();
      flash(copyBtn, "コピーしました");
    }
  });

  // PNG保存機能
  pngBtn.addEventListener("click", async () => {
    const node = document.getElementById("methodWrap");
    try {
      const svg = `
        <svg xmlns='http://www.w3.org/2000/svg' width='800' height='420'>
          <foreignObject width='100%' height='100%'>
            <div xmlns='http://www.w3.org/1999/xhtml' style='font-family: -apple-system, "Segoe UI", Roboto, "Hiragino Kaku Gothic ProN", Meiryo, sans-serif; box-sizing:border-box; padding:24px; width:752px;'>
              ${escapeHtml(node.innerHTML)}
            </div>
          </foreignObject>
        </svg>`;
      const svgBlob = new Blob([svg], { type: "image/svg+xml;charset=utf-8" });
      const url = URL.createObjectURL(svgBlob);
      const img = new Image();
      img.onload = () => {
        const canvas = document.createElement("canvas");
        const scale = 2;
        canvas.width = img.width * scale;
        canvas.height = img.height * scale;
        const ctx = canvas.getContext("2d");
        ctx.fillStyle = getComputedStyle(document.body).backgroundColor || "#fff";
        ctx.fillRect(0, 0, canvas.width, canvas.height);
        ctx.drawImage(img, 0, 0, canvas.width, canvas.height);
        canvas.toBlob((blob) => {
          const a = document.createElement("a");
          a.download = sanitizeFilename(methodTitle.textContent || "節約ダイエット") + ".png";
          a.href = URL.createObjectURL(blob);
          document.body.appendChild(a);
          a.click();
          a.remove();
          URL.revokeObjectURL(url);
          flash(pngBtn, "PNGを保存しました");
        }, "image/png");
      };
      img.onerror = () => {
        flash(pngBtn, "画像化に失敗しました");
        URL.revokeObjectURL(url);
      };
      img.src = url;
    } catch (err) {
      flash(pngBtn, "画像化に失敗しました");
    }
  });

  // SNS/共有ボタン
  function shareURL(platform) {
    const title = encodeURIComponent(methodTitle.textContent || "節約ダイエット");
    const body = encodeURIComponent(methodBody.textContent || "");
    const page = encodeURIComponent(location.href);
    switch (platform) {
      case "x":
        return `https://twitter.com/intent/tweet?text=${title}%0A${body}%0A${page}`;
      case "facebook":
        return `https://www.facebook.com/sharer/sharer.php?u=${page}&quote=${title}%0A${body}`;
      case "line":
        return `https://social-plugins.line.me/lineit/share?url=${page}&text=${title}%0A${body}`;
      case "mail":
        return `mailto:?subject=${title}&body=${body}%0A${page}`;
      default:
        return "#";
    }
  }

  xBtn.addEventListener("click", () => { window.open(shareURL("x"), "_blank"); });
  fbBtn.addEventListener("click", () => { window.open(shareURL("facebook"), "_blank"); });
  lineBtn.addEventListener("click", () => { window.open(shareURL("line"), "_blank"); });
  mailBtn.addEventListener("click", () => { location.href = shareURL("mail"); });

  // 補助関数
  function flash(el, msg) {
    const original = el.dataset.orig || el.textContent;
    el.dataset.orig = original;
    el.textContent = msg;
    el.classList.add("flash");
    setTimeout(() => {
      el.textContent = original;
      el.classList.remove("flash");
    }, 1500);
  }

  function escapeHtml(html) {
    return html.replace(/<(script|style)[\s\S]*?>[\s\S]*?<\/\1>/gi, "")
      .replace(/<!--[\s\S]*?-->/g, "");
  }

  function sanitizeFilename(name) {
    return name.replace(/[\/\\?%*:|"<>]/g, "_").slice(0, 120);
  }

  // 下部に多数の節約ダイエット方法を表示（リスト生成）
  function renderList() {
    const ul = document.createElement("ul");
    ul.className = "methodList";
    methods.forEach((m, idx) => {
      const li = document.createElement("li");
      li.innerHTML = `<strong>${m.name}</strong> - ${stripTags(m.text).slice(0, 120)}...`;
      li.addEventListener("click", () => { showMethod(idx); window.scrollTo({ top: 0, behavior: "smooth" }); });
      ul.appendChild(li);
    });
    listContainer.appendChild(ul);
  }

  function stripTags(html) {
    const d = document.createElement("div");
    d.innerHTML = html;
    return d.textContent || d.innerText || "";
  }

  renderList();

  document.querySelectorAll("a[href^='#']").forEach(a => {
    a.addEventListener("click", (e) => {});
  });

  todayBtn.addEventListener("keyup", (e) => { if (e.key === "Enter") todayBtn.click(); });

