let currentIndex = null;

// 初回ランダム表示
function showRandomRecipe() {
  const idx = Math.floor(Math.random() * RECIPES.length);
  currentIndex = idx;
  displayRecipe(RECIPES[idx]);
}
function displayRecipe(recipe) {
  recipeTitleEl.textContent = recipe.title;
  recipeStepsEl.textContent = recipe.steps;
  // 太字はCSSで制御（ここではテキスト挿入のみ）
}
btnRandom.addEventListener("click", showRandomRecipe);

// コピー機能
copyBtn.addEventListener("click", async () => {
  const text = `${recipeTitleEl.textContent}\n\n${recipeStepsEl.textContent}`;
  try {
    await navigator.clipboard.writeText(text);
    showToast("レシピの文章をコピーしました");
  } catch {
    showToast("コピーに失敗しました");
  }
});

// PNG保存（Canvasで描画）
pngBtn.addEventListener("click", () => {
  const padding = 24;
  const title = recipeTitleEl.textContent || "節約栄養レシピ";
  const steps = recipeStepsEl.textContent || "";
  const lines = (title + "\n\n" + steps).split("\n");
  const fontTitle = "bold 24px sans-serif";
  const fontBody = "16px sans-serif";

  // measure width
  const canvas = document.createElement("canvas");
  const ctx = canvas.getContext("2d");
  ctx.font = fontBody;
  let maxWidth = 0;
  lines.forEach((line, i) => {
    ctx.font = i === 0 ? fontTitle : fontBody;
    maxWidth = Math.max(maxWidth, ctx.measureText(line).width);
  });

  canvas.width = Math.ceil(maxWidth + padding * 2);
  canvas.height = Math.ceil((lines.length * 28) + padding * 2 + 10);
  // background
  ctx.fillStyle = "#fffdfa";
  ctx.fillRect(0, 0, canvas.width, canvas.height);
  // decorative header
  ctx.fillStyle = "#ffcc80";
  ctx.fillRect(0, 0, canvas.width, 10);
  // draw text
  let y = padding + 8;
  lines.forEach((line, i) => {
    ctx.font = i === 0 ? fontTitle : fontBody;
    ctx.fillStyle = i === 0 ? "#2b2b2b" : "#333333";
    ctx.fillText(line, padding, y);
    y += i === 0 ? 34 : 24;
  });

  // ダウンロード
  const url = canvas.toDataURL("image/png");
  const a = document.createElement("a");
  a.href = url;
  a.download = (recipeTitleEl.textContent || "recipe") + ".png";
  a.click();
  showToast("PNGを生成しました");
});

// SNSシェア
function shareToX() {
  const text = encodeURIComponent(recipeTitleEl.textContent + " - " + recipeStepsEl.textContent);
  const url = encodeURIComponent(location.href);
  window.open(`https://twitter.com/intent/tweet?text=${text}&url=${url}`, "_blank");
}
function shareToFB() {
  const url = encodeURIComponent(location.href);
  window.open(`https://www.facebook.com/sharer/sharer.php?u=${url}`, "_blank");
}
function shareToLINE() {
  const text = encodeURIComponent(recipeTitleEl.textContent + "： " + recipeStepsEl.textContent + " " + location.href);
  window.open(`https://social-plugins.line.me/lineit/share?text=${text}`, "_blank");
}
function shareToMail() {
  const subject = encodeURIComponent("節約栄養レシピ: " + recipeTitleEl.textContent);
  const body = encodeURIComponent(recipeStepsEl.textContent + "\n\n" + location.href);
  window.location.href = `mailto:?subject=${subject}&body=${body}`;
}
xBtn.addEventListener("click", shareToX);
fbBtn.addEventListener("click", shareToFB);
lineBtn.addEventListener("click", shareToLINE);
mailBtn.addEventListener("click", shareToMail);

// トースト表示（軽いフィードバック）
function showToast(msg) {
  const t = document.getElementById("toast");
  t.textContent = msg;
  t.classList.add("visible");
  clearTimeout(t._hideTimer);
  t._hideTimer = setTimeout(() => t.classList.remove("visible"), 2500);
}

// レシピ一覧表示（下部）
function populateRecipesGrid(list = RECIPES) {
  recipesGrid.innerHTML = "";
  list.forEach((r, i) => {
    const card = document.createElement("article");
    card.className = "recipe-card";
    card.innerHTML = `
      <h3 class="card-title">${escapeHtml(r.title)}</h3>
      <p class="card-steps">${escapeHtml(r.steps)}</p>
      <button class="card-use" data-idx="${i}" aria-label="このレシピを表示">このレシピを表示</button>
    `;
    recipesGrid.appendChild(card);
  });
  // イベント付与
  recipesGrid.querySelectorAll(".card-use").forEach(b => {
    b.addEventListener("click", (e) => {
      const idx = Number(e.currentTarget.dataset.idx);
      currentIndex = idx;
      displayRecipe(RECIPES[idx]);
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
  });
}
function escapeHtml(s) {
  return s.replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;");
}

// 検索機能（入力で絞り込み）
searchInput.addEventListener("input", (e) => {
  const q = e.target.value.trim().toLowerCase();
  if (!q) {
    populateRecipesGrid();
    return;
  }
  const filtered = RECIPES.filter(r => (r.title + " " + r.steps).toLowerCase().includes(q));
  populateRecipesGrid(filtered);
});

// 初期化
document.addEventListener("DOMContentLoaded", () => {
  populateRecipesGrid();
  showRandomRecipe();
  // アクセシビリティ: Enterでランダム
  btnRandom.addEventListener("keyup", (e) => { if (e.key === "Enter") showRandomRecipe(); });
});