  function adjustEdgePadding() {
    const wrap = document.querySelector(".container");
    if (!wrap) return;
    if (window.innerWidth > window.innerHeight) {
      wrap.style.paddingLeft = "env(safe-area-inset-left, 0px)";
      wrap.style.paddingRight = "env(safe-area-inset-right, 0px)";
    } else {
      wrap.style.paddingLeft = "";
      wrap.style.paddingRight = "";
    }
  }
