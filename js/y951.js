  // レシピ生成関数（オプション：カテゴリによる絞り込みを簡易実装）
  function generateRecipe(mode = "any") {
    // mode: "any" | "light" | "rich"
    let pool = recipes.slice();
    if (mode === "light") {
      pool = pool.filter(r => /大根|和風|レモン|冷製|さっぱり|納豆|オリーブ/.test(r.desc) || /さっぱり|冷|和風|レモン/.test(r.title));
    } else if (mode === "rich") {
      pool = pool.filter(r => /クリーム|バター|濃厚|きのこ|明太子/.test(r.desc) || /クリーム|バター|明太子/.test(r.title));
    }
    if (pool.length === 0) pool = recipes.slice();
    const pick = pool[Math.floor(Math.random() * pool.length)];
    renderRecipe(pick);
  }

  function renderRecipe(obj) {
    // 修正点：タイトルは既に HTML 側にある h2#autoTitle に表示しているため、
    // recipeArea の中にはタイトルを重複して入れないようにする
    titleEl.textContent = obj.title;

    const html = `
      <p class="recipe-desc">${escapeHtml(obj.desc)}</p>
      <h3 class="ingredients-heading">材料</h3>
      <ul class="ingredients-list">
        ${obj.ingredients.map(i => `<li>${escapeHtml(i)}</li>`).join("")}
      </ul>
      <h3 class="howto-heading">作り方（時短ポイント付き）</h3>
      <ol class="howto-list">
        <li>パスタを袋表示より1分短くゆでる。茹で汁を少量残す。</li>
        <li>フライパンで具材をさっと炒め、パスタを加えてソースと和える。</li>
        <li>必要なら茹で汁でソースを延ばす。味を整えて完成。</li>
      </ol>
    `;
    recipeArea.innerHTML = html;
  }

  // コピー機能（レシピのテキストを整形）
  function copyRecipeText() {
    const textItems = [];
    textItems.push(titleEl.textContent || "時短パスタレシピ");
    const desc = recipeArea.querySelector(".recipe-desc");
    if (desc) textItems.push(desc.textContent);
    const ing = recipeArea.querySelectorAll(".ingredients-list li");
    if (ing.length) {
      textItems.push("材料:");
      ing.forEach(li => textItems.push("- " + li.textContent));
    }
    const steps = recipeArea.querySelectorAll(".howto-list li");
    if (steps.length) {
      textItems.push("作り方:");
      steps.forEach((li, idx) => textItems.push((idx+1) + ". " + li.textContent));
    }
    const finalText = textItems.join("\n");
    navigator.clipboard.writeText(finalText).then(() => {
      copyBtn.textContent = "コピーしました！";
      setTimeout(() => copyBtn.textContent = "レシピをコピー", 1500);
    }).catch(() => {
      copyBtn.textContent = "コピー失敗";
      setTimeout(() => copyBtn.textContent = "レシピをコピー", 1500);
    });
  }

  // PNG保存機能（SVG+foreignObject を用いた DOM -> PNG エクスポート）
  async function saveAsPNG() {
    pngBtn.disabled = true;
    pngBtn.textContent = "保存中...";
    try {
      const node = document.getElementById("cardToSave");
      const width = node.offsetWidth;
      const height = node.offsetHeight;

      const serializer = new XMLSerializer();
      const cloned = node.cloneNode(true);
      const wrapper = document.createElement("div");
      wrapper.setAttribute("xmlns", "http://www.w3.org/1999/xhtml");
      wrapper.style.width = width + "px";
      wrapper.style.height = height + "px";
      wrapper.style.boxSizing = "border-box";
      wrapper.appendChild(cloned);

      const svg = `<svg xmlns='http://www.w3.org/2000/svg' width='${width}' height='${height}'>
        <foreignObject width='100%' height='100%'>
          ${new XMLSerializer().serializeToString(wrapper)}
        </foreignObject>
      </svg>`;

      const svgBlob = new Blob([svg], { type: "image/svg+xml;charset=utf-8" });
      const url = URL.createObjectURL(svgBlob);
      const img = new Image();
      img.onload = () => {
        const canvas = document.createElement("canvas");
        canvas.width = width * 2;
        canvas.height = height * 2;
        const ctx = canvas.getContext("2d");
        ctx.scale(2,2);
        ctx.fillStyle = getComputedStyle(document.body).backgroundColor || "#fff";
        ctx.fillRect(0,0,canvas.width,canvas.height);
        ctx.drawImage(img, 0, 0, width, height);
        URL.revokeObjectURL(url);
        canvas.toBlob(blob => {
          const a = document.createElement("a");
          a.download = `${(titleEl.textContent||"timed-pasta").replace(/\s+/g,"_")}.png`;
          a.href = URL.createObjectURL(blob);
          a.click();
          URL.revokeObjectURL(a.href);
          pngBtn.disabled = false;
          pngBtn.textContent = "PNG画像で保存";
        }, "image/png");
      };
      img.onerror = () => {
        alert("画像生成に失敗しました。ブラウザの印刷機能で保存してみてください。");
        pngBtn.disabled = false;
        pngBtn.textContent = "PNG画像で保存";
      };
      img.src = url;
    } catch (e) {
      console.error(e);
      pngBtn.textContent = "エラー";
      setTimeout(() => pngBtn.textContent = "PNG画像で保存", 1500);
      pngBtn.disabled = false;
    }
  }

